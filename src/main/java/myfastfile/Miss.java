package myfastfile;


public class Miss {

    private int packet;
    private long window;

    /**
     * @return the packet
     */
    public int getPacket() {
        return packet;
    }

    /**
     * @param packet the packet to set
     */
    public void setPacket(int packet) {
        this.packet = packet;
    }

    /**
     * @return the window
     */
    public long getWindow() {
        return window;
    }

    /**
     * @param window the window to set
     */
    public void setWindow(long window) {
        this.window = window;
    }

}
